def verify_email_config():
    """Verify email configuration in current environment"""
    print("=== EMAIL CONFIGURATION VERIFICATION ===")
    
    from app import create_app
    
    app = create_app()
    
    with app.app_context():
        # Get email configuration
        config = {
            'MAIL_SERVER': app.config.get('MAIL_SERVER'),
            'MAIL_PORT': app.config.get('MAIL_PORT'),
            'MAIL_USE_TLS': app.config.get('MAIL_USE_TLS'),
            'MAIL_USERNAME': app.config.get('MAIL_USERNAME'),
            'MAIL_PASSWORD': app.config.get('MAIL_PASSWORD'),
            'MAIL_DEFAULT_SENDER': app.config.get('MAIL_DEFAULT_SENDER'),
            'ADMIN_EMAIL': app.config.get('ADMIN_EMAIL')
        }
        
        print("Current Email Configuration:")
        for key, value in config.items():
            if 'PASSWORD' in key and value:
                print(f"  {key}: {'SET' if value else 'NOT SET'}")
            else:
                print(f"  {key}: {value}")
        
        # Check configuration completeness
        required_fields = ['MAIL_SERVER', 'MAIL_USERNAME', 'MAIL_PASSWORD']
        missing_fields = [field for field in required_fields if not config.get(field)]
        
        if missing_fields:
            print(f"\nMissing required fields: {missing_fields}")
            return False, config
        else:
            print(f"\nEmail configuration: COMPLETE")
            return True, config

