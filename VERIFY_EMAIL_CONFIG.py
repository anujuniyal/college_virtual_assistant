def verify_production_config():
    """Verify production (Render) configuration"""
    print("\n=== PRODUCTION CONFIGURATION VERIFICATION ===")
    
    # Check render-docker.yaml configuration
    render_config_file = os.path.join(os.path.dirname(__file__), 'render-docker.yaml')
    
    if os.path.exists(render_config_file):
        print("Checking render-docker.yaml configuration...")
        
        with open(render_config_file, 'r') as f:
            content = f.read()
            
        # Check for email configuration in render-docker.yaml
        email_configs = []
        lines = content.split('\n')
        for line in lines:
            if 'MAIL_' in line and 'value:' in line:
                email_configs.append(line.strip())
        
        print("Email configuration in render-docker.yaml:")
        for config in email_configs:
            if 'PASSWORD' in config:
                print(f"  {config.split(':')[0].strip()}: SET")
            else:
                print(f"  {config}")
        
        # Check if critical email fields are configured with actual values (not placeholders)
        critical_fields = ['MAIL_USERNAME', 'MAIL_PASSWORD', 'MAIL_DEFAULT_SENDER']
        configured_fields = []
        
        for field in critical_fields:
            field_pattern = f"- key: {field}\n        value:"
            if field_pattern in content:
                # Extract the value to check if it's a placeholder
                field_index = content.find(field_pattern)
                if field_index != -1:
                    value_line = content[field_index:field_index + 200].split('\n')[1]
                    value = value_line.split('value:')[1].strip() if 'value:' in value_line else ''
                    
                    if value and not value.startswith('your-') and not value.startswith('UPDATE_'):
                        configured_fields.append(field)
        
        if len(configured_fields) == len(critical_fields):
            print("Production email configuration: COMPLETE")
            return True
        else:
            missing = set(critical_fields) - set(configured_fields)
            print(f"Missing or placeholder production fields: {missing}")
            return False
    else:
        print("render-docker.yaml file not found")
        return False

def verify_local_config():
    """Verify local configuration"""
    print("\n=== LOCAL CONFIGURATION VERIFICATION ===")
    
    # Check .env file
    env_file = os.path.join(os.path.dirname(__file__), '.env')
    
    if os.path.exists(env_file):
        print("Checking .env file...")
        
        with open(env_file, 'r') as f:
            content = f.read()
            
        # Check for email configuration in .env
        email_configs = []
        lines = content.split('\n')
        for line in lines:
            if line.startswith('MAIL_') and '=' in line:
                email_configs.append(line.strip())
        
        print("Email configuration in .env:")
        for config in email_configs:
            if 'PASSWORD' in config:
                print(f"  {config.split('=')[0]}: SET")
            else:
                print(f"  {config}")
        
        # Check if critical email fields are configured
        critical_fields = ['MAIL_SERVER', 'MAIL_USERNAME', 'MAIL_PASSWORD', 'MAIL_DEFAULT_SENDER']
        configured_fields = []
        
        for field in critical_fields:
            if f"{field}=" in content:
                configured_fields.append(field)
        
        if len(configured_fields) == len(critical_fields):
            print("Local email configuration: COMPLETE")
            return True
        else:
            missing = set(critical_fields) - set(configured_fields)
            print(f"Missing local fields: {missing}")
            return False
    else:
        print(".env file not found")
        return False

